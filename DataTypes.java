public class DataTypes{
	public static void main(String[] args){
		System.out.println("Here are some data types"); //String
		System.out.println("20 + 11"); //String
		System.out.println(20 + 11); // Integer
		System.out.println(17.4 - 3.2); // Double
		System.out.println("20" + "4"); // Character
		System.out.println(20 + 4); // Integer
	}
}